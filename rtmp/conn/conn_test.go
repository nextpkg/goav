func TestWriter_Write(t *testing.T) {
	at := assert.New(t)

	i, o := net.Pipe()
	client := &client.ConnClient{
		Conn: &message.Conn{
			Conn:                i,
			Rw:                  comm.NewReadWriter(i, 1024),
			Slab:                slab.NewSlab(),
			ChunkSize:           128,
			RemoteChunkSize:     128,
			WindowAckSize:       2500000,
			RemoteWindowAckSize: 2500000,
			Chunks:              make(map[uint32]*chunk.ChunkStream),
			Option:              message.DefaultOption,
		},
		TransactionID: 0,
	}

	w := NewWriter(client)

	// 视频数据
	p := &packet.Packet{
		Type:     packet.PktVideo,
		StreamID: 1,
		Data:     flvIFrame,
	}
	err := w.Write(p)
	at.Nil(err)

	// 音频数据
	p = &packet.Packet{
		Type:     packet.PktAudio,
		StreamID: 1,
		Data:     avcIFrame,
	}
	err = w.Write(p)
	at.Nil(err)

	go func() {
		time.Sleep(50 * time.Millisecond)
		at.Nil(client.Conn.Flush())
	}()

	buf := make([]byte, 1024)
	n, err := o.Read(buf)
	at.Nil(err)
	at.Equal(461, n)
}

func TestReader_Read(t *testing.T) {
	at := assert.New(t)

	i, o := net.Pipe()
	server := &server.ConnServer{
		StreamID: 1,
		Conn: &message.Conn{
			Conn:                i,
			Rw:                  comm.NewReadWriter(i, 1024),
			Slab:                slab.NewSlab(),
			ChunkSize:           128,
			RemoteChunkSize:     128,
			WindowAckSize:       2500000,
			RemoteWindowAckSize: 2500000,
			Chunks:              make(map[uint32]*chunk.ChunkStream),
			Option:              message.DefaultOption,
		},
	}

	cs := &chunk.ChunkStream{
		Length:   220,
		StreamID: 1,
		TypeID:   9,
		Data:     flvIFrame,
	}
	at.Nil(server.Write(cs))

	go func() {
		at.Nil(server.Conn.Flush())
	}()

	buf := make([]byte, 1024)
	n, err := o.Read(buf)
	at.Nil(err)
	at.Equal(233, n)

	data := buf[:n]

	wg := sync.WaitGroup{}
	wg.Add(1)
	go func() {
		defer wg.Done()
		n, err = o.Write(data)
		at.Nil(err)
		at.Equal(233, n)
	}()

	r := NewReader(server)
	p := &packet.Packet{}
	at.Nil(r.Read(p))

	wg.Wait()
}
